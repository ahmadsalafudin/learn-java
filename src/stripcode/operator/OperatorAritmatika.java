public class OperatorAritmatika {
    
      public static void main(String[] args) {
        //Deklarasi
        int a = 10;
        int b = 20;

        //Deskripsi
        int Jumlah = a + b;
        int Kurangi = a -b;
        int kali = a * b;
        double bagi = (double) a / b;
        

        System.out.println(Jumlah);
        System.out.println(Kurangi);
        System.out.println(kali);
        System.out.println(bagi);
    }
    
}
